package com.example.coffeeapp.model;

public class Coffee {
    int image;
    String rating;
    String name;
    String price;

    public Coffee(int image, String rating, String name, String price) {
        this.image = image;
        this.rating = rating;
        this.name = name;
        this.price = price;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getRating() {
        return rating;
    }

    public void setRating(String rating) {
        this.rating = rating;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
